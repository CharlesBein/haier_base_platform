package cn.gooday.service.common.entity.serialnumber;


/**
 * 存储某表使用的序列号当前最大值
 * @ProjectName: [gooday-common]
 * @Author: [zhangzw]
 * @CreateDate: [2014/11/19 10:05]
 * @Update: [修改实体对应表名为bs_sequence_number] BY[zhangzw][2014/11/25]
 * @Version: [v1.0]
 */
public class SerialNumberGenerateEntity {
    //序号标识
    private Long numberId;
    //为哪个表存储的最大序号
    private String tableName;
    //当前编号
    private Long currentNumber;
    //使用的年份
    private Long currentYear;

    public Long getNumberId() {
        return numberId;
    }

    public void setNumberId(Long numberId) {
        this.numberId = numberId;
    }

    public String getTableName() {
        return tableName;
    }

    public void setTableName(String tableName) {
        this.tableName = tableName;
    }

    public Long getCurrentNumber() {
        return currentNumber;
    }

    public void setCurrentNumber(Long currentNumber) {
        this.currentNumber = currentNumber;
    }

    public Long getCurrentYear() {
        return currentYear;
    }

    public void setCurrentYear(Long currentYear) {
        this.currentYear = currentYear;
    }
}
